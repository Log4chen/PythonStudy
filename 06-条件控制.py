# if elif else，不支持'else if'
'''
if xxx:
    print(1)
elif xxx:
    print(2)
else:
    print(3)
'''

# macth case 类似Java的 switch case
'''
match subject:
    case <pattern_1>:
        <action_1>
    case <pattern_2>:
        <action_2>
    case <pattern_3>:
        <action_3>
    case _:
        <action_wildcard>

"case _"  类似Java中的 default
'''